    @SerialName("hero_id") val heroId: Int? = null,
    @SerialName("icon") val icon: String? = null,
    @SerialName("id") val id: Int? = null,
    @SerialName("img") val img: String? = null,
    @SerialName("int_gain") val intGain: Double? = null,
    @SerialName("legs") val legs: Int? = null,
    @SerialName("localized_name") val localizedName: String? = null,
    @SerialName("move_speed") val moveSpeed: Int? = null,
    @SerialName("name") val name: String? = null,
    @SerialName("null_pick") val nullPick: Int? = null,
    @SerialName("null_win") val nullWin: Int? = null,
    @SerialName("1_pick") val pick1: Int? = null,
    @SerialName("2_pick") val pick2: Int? = null,
