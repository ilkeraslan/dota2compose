        attackType = attackType,
        baseAgi = baseAgi,
        baseArmor = baseArmor,
        baseAttackMax = baseAttackMax,
        baseAttackMin = baseAttackMin,
        baseHealth = baseHealth,
        baseInt = baseInt,
        baseMana = baseMana,
        baseMr = baseMr,
        baseStr = baseStr,
        cmEnabled = cmEnabled,
        heroId = heroId,
        icon = icon,
        id = id,
        img = img,
        legs = legs,
        localizedName = localizedName,
        moveSpeed = moveSpeed,
        name = name,
        primaryAttr = primaryAttr,
        projectileSpeed = projectileSpeed,
        roles = roles
    )
}
