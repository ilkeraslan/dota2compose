    @SerialName("1_win") val win1: Int? = null,
    @SerialName("2_win") val win2: Int? = null,
    @SerialName("3_win") val win3: Int? = null,
    @SerialName("4_win") val win4: Int? = null,
    @SerialName("5_win") val win5: Int? = null,
