    @SerialName("8_pick") val pick8: Int? = null,
    @SerialName("primary_attr") val primaryAttr: String? = null,
    @SerialName("pro_ban") val proBan: Int? = null,
    @SerialName("pro_pick") val proPick: Int? = null,
